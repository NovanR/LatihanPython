# CASTING / Perubahan tipe data

print("====CASTING INTEGER====")
data_int = 10

data_float = float(data_int)
print("Tipe data dari",data_float,"adalah", type(data_float))

data_string = str(data_int)
print("Tipe data dari",data_string,"adalah", type(data_string))

data_boolean = bool(data_int)
print("Tipe data dari",data_boolean,"adalah", type(data_boolean))

print("====CASTING FLOAT====")

data_float2 = 3.14

data_int2 = int(data_float2)
print("Tipe data dari",data_int2,"adalah", type(data_int2))

data_string2 = str(data_float2)
print("Tipe data dari",data_string2,"adalah", type(data_string2))

data_bool2 = bool(data_float2)
print("Tipe data dari",data_bool2,"adalah", type(data_bool2))